//        ", где m — это его предполагаемый курс (число от 1 до 5, считайте, что учатся в университете с 18 лет, курс меняется раз в год).
//        Если это число больше 22, то выведите "
//
//        specialist
//
//        ", что обозначает квалификацию "специалист".
//
//        Входные данные
//        В единственной строке задано целое число n (0 ≤ n ≤ 150).
//
//
//        Выходные данные
//        Выведите искомую строку.

import java.util.Scanner;

public class Ex2011 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        int age = sc.nextInt();

        if (age < 7) {
            System.out.println("preschool child");
        } else if (age >= 7 && age <= 17) {
            System.out.println("schoolchild " + (age - 6));
        } else if (age >= 18 && age <= 22) {
            System.out.println("student " + (age - 17));
        } else {
            System.out.println("specialist");
        }
    }
}