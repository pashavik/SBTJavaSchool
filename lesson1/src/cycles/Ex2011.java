//        Если это число меньше 7, то обычно это обозначает, что задан дошкольник и надо вывести "
//
//        preschool child
//
//        ".
//        Если это число от 7 до 17, то обычно это обозначает, что задан школьник и надо вывести "
//
//        schoolchild m
//
//        ", где m — это его предполагаемый класс (число от 1 до 11, считайте, что учатся в школе с 7 лет, класс меняется раз в год).
//        Если это число от 18 до 22, то обычно это обозначает, что задан студент и надо вывести "
//
//        student m
